package command.garage;

public class GarageDoor {

    public void up() {
        System.out.println("Turning up garage door...");
    }

    public void down() {
        System.out.println("Turning down garage door...");
    }

    public void stop() {
        System.out.println("Stopping garage door");
    }

    public void lightOn() {
        System.out.println("Turning ON garage door light");
    }

    public void lightOff() {
        System.out.println("Turning OFF garage door light");
    }
}
